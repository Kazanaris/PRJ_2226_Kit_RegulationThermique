#define VAL_OC_MAX    1999
//#define RESOLUTION    1000
#define VAL_PWM_MAX   100

//Commande signal PWM résistance de puissance.
// Val_PWM = 0...100
void CommandFan(uint16_t Val_PWM)
{  
    DRV_OC0_PulseWidthSet((Val_PWM / VAL_OC_MAX) * VAL_PWM_MAX);
}

void CommandHeater(uint16_t Val_Commande)
{          
    DRV_OC1_PulseWidthSet((Val_Commande / VAL_OC_MAX) * VAL_PWM_MAX);
}

